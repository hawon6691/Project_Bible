CREATE TABLE deals (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    product_id BIGINT NOT NULL,
    title VARCHAR(255) NOT NULL,
    type VARCHAR(40) NOT NULL DEFAULT 'SPECIAL',
    description CLOB,
    deal_price DECIMAL(15, 2) NOT NULL,
    discount_rate INT NOT NULL DEFAULT 0,
    stock INT NOT NULL DEFAULT 0,
    banner_url VARCHAR(1000),
    status VARCHAR(40) NOT NULL DEFAULT 'ACTIVE',
    start_at TIMESTAMP WITH TIME ZONE NOT NULL,
    end_at TIMESTAMP WITH TIME ZONE NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_deals_product FOREIGN KEY (product_id) REFERENCES products(id) ON DELETE CASCADE
);

CREATE TABLE recommendations (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    product_id BIGINT NOT NULL,
    target_type VARCHAR(40) NOT NULL DEFAULT 'TRENDING',
    target_user_id BIGINT,
    reason VARCHAR(500),
    score DECIMAL(10, 2) NOT NULL DEFAULT 0,
    active BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_recommendations_product FOREIGN KEY (product_id) REFERENCES products(id) ON DELETE CASCADE,
    CONSTRAINT fk_recommendations_user FOREIGN KEY (target_user_id) REFERENCES users(id) ON DELETE CASCADE
);

CREATE INDEX idx_deals_product_id ON deals(product_id);
CREATE INDEX idx_deals_status ON deals(status);
CREATE INDEX idx_recommendations_target_type ON recommendations(target_type);
CREATE INDEX idx_recommendations_target_user_id ON recommendations(target_user_id);
