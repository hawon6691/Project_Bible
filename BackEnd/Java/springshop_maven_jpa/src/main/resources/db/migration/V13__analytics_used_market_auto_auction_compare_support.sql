CREATE TABLE used_market_prices (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    product_id BIGINT NOT NULL,
    price DECIMAL(15, 2) NOT NULL,
    condition_grade VARCHAR(20) NOT NULL DEFAULT 'GOOD',
    source VARCHAR(120),
    created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_used_market_prices_product FOREIGN KEY (product_id) REFERENCES products(id) ON DELETE CASCADE
);

CREATE TABLE auto_models (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    brand VARCHAR(120) NOT NULL,
    name VARCHAR(120) NOT NULL,
    type VARCHAR(40) NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE auto_trims (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    auto_model_id BIGINT NOT NULL,
    name VARCHAR(120) NOT NULL,
    base_price DECIMAL(15, 2) NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_auto_trims_model FOREIGN KEY (auto_model_id) REFERENCES auto_models(id) ON DELETE CASCADE
);

CREATE TABLE auto_options (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    auto_trim_id BIGINT NOT NULL,
    name VARCHAR(120) NOT NULL,
    price DECIMAL(15, 2) NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_auto_options_trim FOREIGN KEY (auto_trim_id) REFERENCES auto_trims(id) ON DELETE CASCADE
);

CREATE TABLE auto_lease_offers (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    auto_model_id BIGINT NOT NULL,
    provider VARCHAR(120) NOT NULL,
    monthly_payment DECIMAL(15, 2) NOT NULL,
    contract_months INT NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_auto_lease_offers_model FOREIGN KEY (auto_model_id) REFERENCES auto_models(id) ON DELETE CASCADE
);

CREATE TABLE auctions (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL,
    category_id BIGINT,
    title VARCHAR(255) NOT NULL,
    description CLOB NOT NULL,
    specs_json CLOB,
    budget DECIMAL(15, 2),
    status VARCHAR(20) NOT NULL DEFAULT 'OPEN',
    selected_bid_id BIGINT,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_auctions_user FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE,
    CONSTRAINT fk_auctions_category FOREIGN KEY (category_id) REFERENCES categories(id) ON DELETE SET NULL
);

CREATE TABLE auction_bids (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    auction_id BIGINT NOT NULL,
    user_id BIGINT NOT NULL,
    price DECIMAL(15, 2) NOT NULL,
    description CLOB,
    delivery_days INT,
    status VARCHAR(20) NOT NULL DEFAULT 'ACTIVE',
    created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_auction_bids_auction FOREIGN KEY (auction_id) REFERENCES auctions(id) ON DELETE CASCADE,
    CONSTRAINT fk_auction_bids_user FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE
);

